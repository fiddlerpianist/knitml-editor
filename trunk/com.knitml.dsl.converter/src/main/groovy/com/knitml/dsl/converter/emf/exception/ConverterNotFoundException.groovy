package com.knitml.dsl.converter.emf.exception

class ConverterNotFoundException extends ConversionException {

	public ConverterNotFoundException() {
		super()
	}

	public ConverterNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1)
	}

	public ConverterNotFoundException(String arg0) {
		super(arg0)
	}

	public ConverterNotFoundException(Throwable arg0) {
		super(arg0)
	}

}
