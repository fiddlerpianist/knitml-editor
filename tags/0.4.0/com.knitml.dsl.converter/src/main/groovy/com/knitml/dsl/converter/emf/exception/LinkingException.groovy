package com.knitml.dsl.converter.emf.exception

class LinkingException extends ConversionException {

	public LinkingException() {
		super()
	}

	public LinkingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LinkingException(String arg0) {
		super(arg0)
	}

	public LinkingException(Throwable arg0) {
		super(arg0)
	}
}
